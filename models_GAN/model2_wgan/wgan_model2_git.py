def generator_block(in_channels, out_channels):
    #Upsample + Conv2d en lugar de ConvTranspose2d para evitar ruido ajedrezz
    return nn.Sequential(
        nn.Upsample(scale_factor=2, mode="nearest"),
        nn.Conv2d(in_channels, out_channels, kernel_size=3, stride=1, padding=1),
        nn.BatchNorm2d(out_channels),
        nn.ReLU(inplace=True),
        nn.Conv2d(out_channels, out_channels, kernel_size=3, stride=1, padding=1),
        nn.BatchNorm2d(out_channels),
        nn.ReLU(inplace=True),
    )


class Generator(nn.Module):

    def __init__(self, latent_dim=128, base_channels=64):
        super().__init__()
        self.init_size = 6
        c_init = base_channels * 8

        self.fc = nn.Linear(latent_dim, c_init * self.init_size * self.init_size)
        self.stem = nn.Sequential(
            nn.Conv2d(c_init, c_init, kernel_size=3, stride=1, padding=1),
            nn.BatchNorm2d(c_init),
            nn.ReLU(inplace=True),
        )

        self.block1 = generator_block(c_init, base_channels * 4)            # 6 -->12,  256 canal
        self.block2 = generator_block(base_channels * 4, base_channels * 2) # 12-->24, 128 can
        self.block3 = generator_block(base_channels * 2, base_channels)     # 24-->48,  64 can
        self.block4 = generator_block(base_channels, base_channels)         # 48-->96,  64 can

        self.to_image = nn.Sequential(
            nn.Conv2d(base_channels, 1, kernel_size=3, stride=1, padding=1),
            nn.Tanh(),
        )

    def forward(self, z):
        x = self.fc(z)
        x = x.view(z.size(0), -1, self.init_size, self.init_size)
        x = self.stem(x)
        x = self.block1(x)
        x = self.block2(x)
        x = self.block3(x)
        x = self.block4(x)
        return self.to_image(x)

def snconv(in_channels, out_channels, kernel_size, stride, padding):
    #spectral norm
    return nn.utils.spectral_norm(
        nn.Conv2d(in_channels, out_channels, kernel_size, stride, padding)
    )


class Critic(nn.Module):

    def __init__(self, base_channels=64):
        super().__init__()
        layers = [
            # 96 -> 48
            snconv(1, base_channels, 4, 2, 1),
            nn.LeakyReLU(0.2, inplace=True),
            snconv(base_channels, base_channels, 3, 1, 1),
            nn.LeakyReLU(0.2, inplace=True),

            # 48 -> 24
            snconv(base_channels, base_channels * 2, 4, 2, 1),
            nn.LeakyReLU(0.2, inplace=True),
            snconv(base_channels * 2, base_channels * 2, 3, 1, 1),
            nn.LeakyReLU(0.2, inplace=True),

            # 24 -> 12
            snconv(base_channels * 2, base_channels * 4, 4, 2, 1),
            nn.LeakyReLU(0.2, inplace=True),
            snconv(base_channels * 4, base_channels * 4, 3, 1, 1),
            nn.LeakyReLU(0.2, inplace=True),

            # 12 -> 6
            snconv(base_channels * 4, base_channels * 8, 4, 2, 1),
            nn.LeakyReLU(0.2, inplace=True),
            snconv(base_channels * 8, base_channels * 8, 3, 1, 1),
            nn.LeakyReLU(0.2, inplace=True),

            # 6 -> 1
            snconv(base_channels * 8, 1, 6, 1, 0),
            nn.Flatten(),
        ]
        self.model = nn.Sequential(*layers)

    def forward(self, x):
        return self.model(x)

def histogram_to_iq_points(hist, num_points=2048, value_range=(-4.5, 4.5)):
    #Muestrea num_points puntos IQ a partir de un histograma
    if hist.ndim == 3:
        hist = hist.squeeze()
    hist = np.asarray(hist, dtype=np.float32)
    hist = hist - hist.min()
    total = hist.sum()
    if total <= 0:
        flat_prob = np.full(hist.size, 1.0 / hist.size, dtype=np.float32)
    else:
        flat_prob = (hist / total).flatten()

    sampled_idx = np.random.choice(len(flat_prob), size=num_points, p=flat_prob)
    rows, cols = np.unravel_index(sampled_idx, hist.shape)
    min_val, max_val = value_range
    x = min_val + (rows / hist.shape[0]) * (max_val - min_val)
    y = min_val + (cols / hist.shape[1]) * (max_val - min_val)
    pixel_size_x = (max_val - min_val) / hist.shape[0]
    pixel_size_y = (max_val - min_val) / hist.shape[1]
    x += np.random.uniform(-pixel_size_x / 2, pixel_size_x / 2, size=num_points)
    y += np.random.uniform(-pixel_size_y / 2, pixel_size_y / 2, size=num_points)
    return x, y

