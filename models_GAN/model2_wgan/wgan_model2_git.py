#guardar JSON
def serializable_subset(results):
    keys = ["roc_auc", "pr_auc",
            "mean_real", "std_real", "mean_generated", "std_generated",
            "threshold_p5", "threshold_3sigma",
            "best_threshold", "best_precision", "best_recall", "best_f1",
            "metrics_p5", "metrics_3sigma", "metrics_best"]
    return {k: results[k] for k in keys}


def build_metrics_text(results, header=""):
    lines = []
    if header:
        lines.append(header)
        lines.append("-" * len(header))
    lines.extend([
        f"ROC AUC: {results['roc_auc']:.4f}",
        f"PR AUC: {results['pr_auc']:.4f}",
        f"Mean anomaly score (reference):  {results['mean_real']:.4f} (std {results['std_real']:.4f})",
        f"Mean anomaly score (generated):  {results['mean_generated']:.4f} (std {results['std_generated']:.4f})",
        "",
        f"Best F1: {results['best_f1']:.4f} @ threshold = {results['best_threshold']:.4f}",
        f"  Precision: {results['metrics_best']['precision']:.4f} | "
        f"Recall: {results['metrics_best']['recall']:.4f}",
        "",
        f"Threshold percentil 5 (sobre reference): {results['threshold_p5']:.4f}",
        f"  Detection rate: {results['metrics_p5']['detection_rate']:.4f} | "
        f"FPR: {results['metrics_p5']['fpr']:.4f}",
        f"Threshold estad (sobre reference): {results['threshold_3sigma']:.4f}",
        f"  Detection rate: {results['metrics_3sigma']['detection_rate']:.4f} | "
        f"FPR: {results['metrics_3sigma']['fpr']:.4f}",
    ])
    return "\n".join(lines)

