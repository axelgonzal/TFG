def gradient_penalty(critic, real, fake, device):
#funcion objetivo
    batch_size = real.size(0)
    epsilon = torch.rand(batch_size, 1, 1, 1, device=device)
    interpolated = epsilon * real + (1 - epsilon) * fake
    interpolated.requires_grad_(True)
    d_interpolated = critic(interpolated)
    gradients = torch.autograd.grad(
        outputs=d_interpolated,
        inputs=interpolated,
        grad_outputs=torch.ones_like(d_interpolated),
        create_graph=True,
        retain_graph=True,
    )[0]
    gradients = gradients.view(batch_size, -1)
    gp = ((gradients.norm(2, dim=1) - 1) ** 2).mean()
    return gp


def train_wgan_gp(generator, critic, dataloader, optimizer_G, optimizer_C,
                  num_epochs, latent_dim, device,
                  lambda_gp=10.0, n_critic=5,
                  collapse_patience=20, collapse_gap_eps=1e-4):

    history = {
        "loss_G": [], "loss_D": [],
        "real_score": [], "fake_score": [], "critic_gap": [],
    }
    collapse_counter = 0

    generator.train()
    critic.train()

    print(f"WGAN-GP training: epochs={num_epochs}, n_critic={n_critic}, "
          f"lambda_gp={lambda_gp}, device={device}")

    for epoch in range(num_epochs):
        epoch_g_losses = []
        epoch_d_losses = []
        epoch_real_scores = []
        epoch_fake_scores = []

        for batch_idx, real_imgs in enumerate(dataloader):
            real_imgs = real_imgs.to(device)
            batch_size = real_imgs.size(0)


           #act. critico
            z = torch.randn(batch_size, latent_dim, device=device)
            fake_imgs = generator(z).detach()

            real_validity = critic(real_imgs)
            fake_validity = critic(fake_imgs)
            gp = gradient_penalty(critic, real_imgs, fake_imgs, device)
            loss_D = -(real_validity.mean() - fake_validity.mean()) + lambda_gp * gp

            optimizer_C.zero_grad(set_to_none=True)
            loss_D.backward()
            optimizer_C.step()

            epoch_d_losses.append(loss_D.item())
            epoch_real_scores.append(real_validity.mean().item())
            epoch_fake_scores.append(fake_validity.mean().item())

            # act. generador cada n_critic veces
            if batch_idx % n_critic == 0:
                z = torch.randn(batch_size, latent_dim, device=device)
                fake_imgs = generator(z)
                loss_G = -critic(fake_imgs).mean()

                optimizer_G.zero_grad(set_to_none=True)
                loss_G.backward()
                optimizer_G.step()
                epoch_g_losses.append(loss_G.item())

        epoch_g_loss = float(np.mean(epoch_g_losses)) if epoch_g_losses else float("nan")
        epoch_d_loss = float(np.mean(epoch_d_losses)) if epoch_d_losses else float("nan")
        epoch_real_score = float(np.mean(epoch_real_scores)) if epoch_real_scores else float("nan")
        epoch_fake_score = float(np.mean(epoch_fake_scores)) if epoch_fake_scores else float("nan")
        critic_gap = epoch_real_score - epoch_fake_score

        history["loss_G"].append(epoch_g_loss)
        history["loss_D"].append(epoch_d_loss)
        history["real_score"].append(epoch_real_score)
        history["fake_score"].append(epoch_fake_score)
        history["critic_gap"].append(critic_gap)

        print(f"Epoch [{epoch+1}/{num_epochs}] | "
              f"Loss D: {epoch_d_loss:.4f} | Loss G: {epoch_g_loss:.4f} | "
              f"Real: {epoch_real_score:.4f} | Fake: {epoch_fake_score:.4f} | "
              f"Gap (W est.): {critic_gap:.4f}")

        if not np.isfinite(epoch_d_loss) or not np.isfinite(epoch_g_loss):
            print("Stopping: pérdidas no finitas.")
            break

        if abs(critic_gap) < collapse_gap_eps:
            collapse_counter += 1
        else:
            collapse_counter = 0

        if collapse_counter >= collapse_patience:
            print(f"Stopping: crítico colapsado (gap≈0 durante {collapse_patience} epochs).")
            break

    return history

def scores_from_loader(critic, dataloader, device):
    scores = []
    critic.eval()
    with torch.no_grad():
        for imgs in dataloader:
            imgs = imgs.to(device)
            out = critic(imgs).cpu().numpy().flatten()
            scores.extend(out)
    return np.array(scores)


def scores_from_generator_components(generator, critic, latent_dim,
                                     n_samples, device, batch_size=64):
    scores = []
    generator.eval()
    critic.eval()
    n_done = 0
    with torch.no_grad():
        while n_done < n_samples:
            b = min(batch_size, n_samples - n_done)
            z = torch.randn(b, latent_dim, device=device)
            fakes = generator(z)
            s = critic(fakes).cpu().numpy().flatten()
            scores.extend(s)
            n_done += b
    return np.array(scores)


def evaluate_components(generator, critic, reference_loader, latent_dim,
                        n_samples, device):
    print("  Computing scores on reference (loader a critic)...")
    scores_real = scores_from_loader(critic, reference_loader, device)
    print("  Generating and scoring synthetic samples (z --> G --> critic)...")
    scores_generated = scores_from_generator_components(
        generator, critic, latent_dim, n_samples, device
    )
    return scores_real, scores_generated

def classification_metrics(y_true, y_score, threshold):
    y_pred = (y_score >= threshold).astype(int)
    tp = int(np.sum((y_true == 1) & (y_pred == 1)))
    fp = int(np.sum((y_true == 0) & (y_pred == 1)))
    tn = int(np.sum((y_true == 0) & (y_pred == 0)))
    fn = int(np.sum((y_true == 1) & (y_pred == 0)))
    rec = float(recall_score(y_true, y_pred, zero_division=0))
    return {
        "precision": float(precision_score(y_true, y_pred, zero_division=0)),
        "recall": rec,
        "f1": float(f1_score(y_true, y_pred, zero_division=0)),
        "fpr": float(fp / (fp + tn + 1e-12)),
        "detection_rate": rec,
        "tp": tp, "fp": fp, "tn": tn, "fn": fn,
    }


def best_f1_from_pr(y_true, y_score):
    precision, recall, thresholds = precision_recall_curve(y_true, y_score)
    if len(thresholds) == 0:
        return float("nan"), float("nan"), float("nan"), float("nan")
    f1_scores = (2 * precision[:-1] * recall[:-1]) / (precision[:-1] + recall[:-1] + 1e-12)
    best_idx = int(np.argmax(f1_scores))
    return (float(thresholds[best_idx]),
            float(precision[best_idx]),
            float(recall[best_idx]),
            float(f1_scores[best_idx]))


def compute_metrics(scores_real, scores_generated, threshold_percentile=5):
    #anomaly = -critic.
    anomaly_real = -np.asarray(scores_real)
    anomaly_generated = -np.asarray(scores_generated)

    y_true = np.concatenate([np.zeros_like(anomaly_real),
                             np.ones_like(anomaly_generated)])
    y_score = np.concatenate([anomaly_real, anomaly_generated])

    roc_fpr, roc_tpr, _ = roc_curve(y_true, y_score)
    roc_auc = float(roc_auc_score(y_true, y_score))
    pr_precision, pr_recall, _ = precision_recall_curve(y_true, y_score)
    pr_auc = float(average_precision_score(y_true, y_score))

    mean_r = float(np.mean(anomaly_real))
    std_r = float(np.std(anomaly_real))
    threshold_3sigma = mean_r + 3.0 * std_r
    threshold_p5 = float(np.percentile(anomaly_real, 100 - threshold_percentile))

    best_th, best_p, best_r, best_f1 = best_f1_from_pr(y_true, y_score)

    metrics_3sigma = classification_metrics(y_true, y_score, threshold_3sigma)
    metrics_p5 = classification_metrics(y_true, y_score, threshold_p5)
    metrics_best = classification_metrics(y_true, y_score, best_th)

    return {
        # Arrays
        "anomaly_real": anomaly_real,
        "anomaly_generated": anomaly_generated,
        "y_true": y_true, "y_score": y_score,
        "roc_fpr": roc_fpr, "roc_tpr": roc_tpr,
        "pr_precision": pr_precision, "pr_recall": pr_recall,
        # Escalares
        "roc_auc": roc_auc, "pr_auc": pr_auc,
        "mean_real": mean_r, "std_real": std_r,
        "mean_generated": float(np.mean(anomaly_generated)),
        "std_generated": float(np.std(anomaly_generated)),
        "threshold_p5": threshold_p5,
        "threshold_3sigma": threshold_3sigma,
        "best_threshold": float(best_th),
        "best_precision": float(best_p),
        "best_recall": float(best_r),
        "best_f1": float(best_f1),
        "metrics_p5": metrics_p5,
        "metrics_3sigma": metrics_3sigma,
        "metrics_best": metrics_best,
    }

