def compute_attacker_stats(real_hists_arr, generated_hists_arr):
    #calculo parametros muestras generadass

    real_flat = np.asarray(real_hists_arr).reshape(-1).astype(np.float64)
    gen_flat = np.asarray(generated_hists_arr).reshape(-1).astype(np.float64)
    return {
        "real_test": {
            "mean": float(np.mean(real_flat)),
            "std": float(np.std(real_flat)),
            "min": float(np.min(real_flat)),
            "max": float(np.max(real_flat)),
            "n_samples": int(real_hists_arr.shape[0]),
        },
        "generated": {
            "mean": float(np.mean(gen_flat)),
            "std": float(np.std(gen_flat)),
            "min": float(np.min(gen_flat)),
            "max": float(np.max(gen_flat)),
            "n_samples": int(generated_hists_arr.shape[0]),
        },
        "absolute_differences": {
            "mean": float(abs(np.mean(real_flat) - np.mean(gen_flat))),
            "std": float(abs(np.std(real_flat) - np.std(gen_flat))),
        },
    }


def build_metrics_json(split_info, attacker_stats, results_A):

    def defender_block(res):
        return {
            "score_distribution": {
                "real_test":  {"mean": res["mean_real"],
                               "std": res["std_real"]},
                "generated":  {"mean": res["mean_generated"],
                               "std": res["std_generated"]},
            },
            "roc_auc": res["roc_auc"],
            "pr_auc": res["pr_auc"],
            "best_f1": {
                "f1": res["best_f1"],
                "precision": res["best_precision"],
                "recall": res["best_recall"],
                "threshold": res["best_threshold"],
            },
            "threshold_percentile_5": res["metrics_p5"],
            "threshold_mu_plus_3sigma": res["metrics_3sigma"],
        }

    return {
        "split": split_info,
        "attacker": attacker_stats,
        "defender_components_A": defender_block(results_A),
    }

