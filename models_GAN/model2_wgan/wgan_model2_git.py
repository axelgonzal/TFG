def create_histogram(x, y, resolution=96, value_range=(-4.5, 4.5)):

    H, _, _ = np.histogram2d(
        x, y,
        bins=resolution,
        range=[[value_range[0], value_range[1]],
               [value_range[0], value_range[1]]],
    )
    if H.max() > 0:
        H = H / H.max()
    H = H * 2 - 1
    return H


def list_csv_files(folder):
    return sorted([f for f in os.listdir(folder) if f.endswith(".csv")])


def enumerate_sample_keys(folder, min_points=100):

    keys = []
    files = list_csv_files(folder)
    for file in files:
        path = os.path.join(folder, file)
        sample_ids_col = pd.read_csv(path, usecols=["Sample_Id"])["Sample_Id"]
        counts = sample_ids_col.value_counts()
        for sid, count in counts.items():
            if count >= min_points:
                keys.append((file, int(sid)))
    return keys


def compute_train_test_split(folder, test_fraction=0.2, seed=42, min_points=100):

    keys = enumerate_sample_keys(folder, min_points=min_points)

    # Agrupar claves por archivo (cada archivo = una distancia)
    keys_by_file = {}
    for key in keys:
        file = key[0]
        keys_by_file.setdefault(file, []).append(key)

    rng = np.random.default_rng(seed)
    train_keys, test_keys = set(), set()
    test_per_file = {}
    for file in sorted(keys_by_file.keys()):     # sort para determinismo
        file_keys = keys_by_file[file]
        indices = np.arange(len(file_keys))
        rng.shuffle(indices)
        n_test = int(round(len(file_keys) * test_fraction))
        for rank, idx in enumerate(indices):
            if rank < n_test:
                test_keys.add(file_keys[idx])
            else:
                train_keys.add(file_keys[idx])
        test_per_file[file] = n_test


    print(f"Split  por archivo (distancia):")
    print(f"  Archivos: {len(keys_by_file)} | "
          f"Total samples válidos: {len(keys)}")
    print(f"  Train: {len(train_keys)} samples | Test: {len(test_keys)} samples")
    print(f"  Test por archivo: {sorted(set(test_per_file.values()))} "
          f"(seed={seed}, test_fraction={test_fraction})")
    return train_keys, test_keys


class IQHistogramDataset(Dataset):

    def __init__(self, files, folder, resolution=96, value_range=(-4.5, 4.5),
                 min_points=100, allowed_keys=None):
        self.data = []
        self.metadata = []

        for file in files:
            path = os.path.join(folder, file)
            df = pd.read_csv(path)
            for sample_id in df["Sample_Id"].unique():
                key = (file, int(sample_id))
                if allowed_keys is not None and key not in allowed_keys:
                    continue
                df_sample = df[df["Sample_Id"] == sample_id]
                x = df_sample["Real"].values
                y = df_sample["Imag"].values
                if len(x) < min_points:
                    continue
                hist = create_histogram(x, y, resolution=resolution,
                                        value_range=value_range)
                self.data.append(hist)
                self.metadata.append({
                    "file": file,
                    "sample_id": int(sample_id),
                    "n_points": int(len(x)),
                })

    def __len__(self):
        return len(self.data)

    def __getitem__(self, idx):
        img = torch.tensor(self.data[idx], dtype=torch.float32).unsqueeze(0)
        return img


