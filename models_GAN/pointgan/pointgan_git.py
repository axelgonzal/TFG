def build_pointclouds_from_path(data_path, num_points, seed,
                                 inject_beacons_flag=False,
                                 fixed_beacons_dict=None,
                                 split_dict=None,
                                 partition="train"):
    rng = np.random.default_rng(seed)
    samples = []
    meta = []

    for source_file in list_source_files(data_path):
        path = os.path.join(data_path, source_file)
        df = pd.read_csv(path)

        if split_dict is not None:
            df = filter_samples_by_split(df, source_file, split_dict, partition)
        if df.empty:
            continue

        sample_ids = sorted(df["Sample_Id"].unique())
        distance = parse_distance_from_source(source_file)
        beacons_this_dist = (fixed_beacons_dict or {}).get(distance, {}) if inject_beacons_flag else {}

        for sample_id in sample_ids:
            sample_df = df[df["Sample_Id"] == sample_id]

            if inject_beacons_flag and beacons_this_dist:
                sample_df_marked = inject_beacons_into_sample(sample_df, beacons_this_dist)
                points = sample_df_marked[["Real", "Imag"]].values
            else:
                points = sample_df[["Real", "Imag"]].values

            pc = sample_point_cloud(points, num_points, rng)
            samples.append(pc.astype(np.float32))
            meta.append({
                "source": source_file,
                "distance": distance,
                "sample_id": sample_id,
            })

    return np.stack(samples), pd.DataFrame(meta)


