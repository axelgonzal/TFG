def generate_beacon_positions(
    x,
    y,
    fi=0.7,
    angles=None,
    p_low=1,
    p_high=99
):
 #funcion que genera puntos baliza a partir de las coord. normalizadas

    x = np.asarray(x)
    y = np.asarray(y)

    if angles is None:
        angles = np.linspace(0, 2 * np.pi, 4, endpoint=False)

    x_min = np.percentile(x, p_low)
    x_max = np.percentile(x, p_high)
    y_min = np.percentile(y, p_low)
    y_max = np.percentile(y, p_high)

    cx = (x_min + x_max) / 2.0
    cy = (y_min + y_max) / 2.0
    #agafa el radi minim des del centre dels percentils a un dels punts dels percentils
    r = min(
        abs(x_max - cx),
        abs(cx - x_min),
        abs(y_max - cy),
        abs(cy - y_min),
    )
    #reduccio radi
    r2 = fi * r
    beacon_points = []
    for theta in angles:
        bx = cx + r2 * np.cos(theta)
        by = cy + r2 * np.sin(theta)
        beacon_points.append([bx, by])

    beacon_points = np.asarray(beacon_points)
    metadata = {
        "center": (cx, cy),
        "radius_original": r,
        "radius_reduced": r2,
        "bounds": {
            "x_min": x_min,
            "x_max": x_max,
            "y_min": y_min,
            "y_max": y_max,
        },
    }

    return beacon_points, metadata

def count_beacons(
    x,
    y,
    beacon_points,
    epsilon=0.03
):

    x = np.asarray(x)
    y = np.asarray(y)
    #ajunta punts en un vector x,y
    points = np.column_stack([x, y])
    detected = []
    #calcula distancia entre punts i balisses, si es menor a epsilon es considera detectat
    for i, beacon in enumerate(beacon_points):
        distances = np.linalg.norm(points - beacon, axis=1)
        if np.any(distances < epsilon):
            detected.append(i)

    return len(detected), detected

