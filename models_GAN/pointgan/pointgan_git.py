# Eliminación de balizas de una muestra
def remove_beacons_from_sample(sample_df, beacons_per_cluster, eps):

    if not beacons_per_cluster:
        return sample_df.copy(), 0

    rows_to_drop = []

    for q, beacon_positions in beacons_per_cluster.items():
        cluster_mask = sample_df["Q"] == q
        cluster_df = sample_df[cluster_mask]
        if cluster_df.empty:
            continue

        # Coordenadas normalizadas de los puntos del q
        points = cluster_df[["X_norm", "Y_norm"]].values  #

        # Calcular dist de cada punto a cualquier baliza esperada
        diffs = points[:, None, :] - beacon_positions[None, :, :]
        dists = np.linalg.norm(diffs, axis=2)  # (N_points, N_beacons)
        min_dist_per_point = dists.min(axis=1)  # (N_points,)

        # eliminar
        is_beacon_point = min_dist_per_point < eps
        rows_to_drop.extend(cluster_df.index[is_beacon_point].tolist())

    n_removed = len(rows_to_drop)
    clean_df = sample_df.drop(index=rows_to_drop).reset_index(drop=True)
    return clean_df, n_removed

