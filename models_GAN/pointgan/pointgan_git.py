class PointGANGenerator(nn.Module):
    def __init__(self, latent_dim, num_points):
        super().__init__()
        self.num_points = num_points
        self.net = nn.Sequential(
            nn.Linear(latent_dim, 256),
            nn.LeakyReLU(0.2),
            nn.Linear(256, 512),
            nn.LeakyReLU(0.2),
            nn.Linear(512, 1024),
            nn.LeakyReLU(0.2),
            nn.Linear(1024, 2048),
            nn.LeakyReLU(0.2),
            nn.Linear(2048, num_points * 2),
        )

    def forward(self, z):
        x = self.net(z)
        return x.view(-1, self.num_points, 2)


class PointGANDiscriminator(nn.Module):
  """
  La xarxa MLP s'aplica a cada punt individual, s'extreu les característiques i
  s'agrega a un vector general a partir de max pooling. Aquest ultim passa pel classificador
  produint un valor logit, que passara per sigmoid i sera una probabilitat
  """
  def __init__(self, input_dim=2):
        super().__init__()
        self.mlp = nn.Sequential(
            nn.Linear(input_dim, 64),
            nn.LeakyReLU(0.2),
            nn.Linear(64, 128),
            nn.LeakyReLU(0.2),
            nn.Linear(128, 256),
            nn.LeakyReLU(0.2),
            nn.Linear(256, 512),
            nn.LeakyReLU(0.2),
        )
        self.fc = nn.Sequential(
            nn.Linear(1024, 256),  # 512 max + 512 mean = 1024
            nn.LeakyReLU(0.2),
            nn.Dropout(0.3),
            nn.Linear(256, 128),
            nn.LeakyReLU(0.2),
            nn.Linear(128, 1),
        )

  def forward(self, x):
        x = self.mlp(x)                          # (batch, num_points, 512)
        x_max = torch.max(x, dim=1).values       # (batch, 512)
        x_mean = torch.mean(x, dim=1)            # (batch, 512)
        x_global = torch.cat([x_max, x_mean], dim=1)  # (batch, 1024)
        return self.fc(x_global).squeeze(1)

#creem dos maps: un dist--> thresh i un altre de grup per si a cas
def build_threshold_maps(anchor_summary_df):
    threshold_by_distance = {}
    if anchor_summary_df.empty:
        return threshold_by_distance, {}
    for _, row in anchor_summary_df.iterrows():
        threshold_by_distance[int(row["distance"])] = float(row["threshold"])
    group_threshold = (
        anchor_summary_df
        .groupby("group")["threshold"]
        .mean()
        .to_dict()
    )
    return threshold_by_distance, group_threshold

#retornem el threshold depenent de la dist, si no existeix el de grup
def threshold_for_distance(distance, threshold_by_distance, group_threshold):
    if not threshold_by_distance:
        return float("inf")  # En experimento A, threshold inalcanzable -> beacon nunca marca
    if distance in threshold_by_distance:
        return threshold_by_distance[distance]
    group = group_for_distance(distance)
    return float(group_threshold.get(group, float("inf")))


#normalitzar tamany nuvol a 2048
def sample_point_cloud(points, num_points, rng):
    if len(points) == 0:
        return np.zeros((num_points, 2), dtype=np.float32)
    if len(points) >= num_points:
        idx = rng.choice(len(points), size=num_points, replace=False)
        return points[idx].astype(np.float32)
    pad_idx = rng.choice(len(points), size=num_points - len(points), replace=True)
    padded = np.concatenate([points, points[pad_idx]], axis=0)
    return padded.astype(np.float32)


