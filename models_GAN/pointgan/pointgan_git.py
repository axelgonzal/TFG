    gen_df = pd.read_csv(generated_csv_path)
    sample_ids = sorted(gen_df["Sample_Id"].unique())

    distances = [parse_distance_from_source(f) for f in list_source_files(DATA_PATH)]

    # asignar cada fake a una distancia aleatoria
    sample_to_distance = {sid: int(rng.choice(distances)) for sid in sample_ids}

    # agrupar fakes por distancia asignada
    fakes_per_distance = {d: [] for d in distances}
    for sid, d in sample_to_distance.items():
        fakes_per_distance[d].append(sid)

    #  submuestrear n_per_distance por distancia
    selected_fakes = {}
    for d, fakes in fakes_per_distance.items():
        if len(fakes) >= n_per_distance:
            chosen = list(rng.choice(fakes, size=n_per_distance, replace=False))
        else:
            chosen = fakes
        selected_fakes[d] = chosen

    total_selected = sum(len(v) for v in selected_fakes.values())
    print(f"Selected {total_selected} fakes "
          f"(target: {len(distances)} x {n_per_distance} = {len(distances)*n_per_distance})")

    # evaluar fakes seleccionados contra su distancia asignada
    with torch.no_grad():
        for lb, sample_ids_for_lb in selected_fakes.items():
            eps, _ = epsilon_for_distance(lb)
            threshold = threshold_for_distance(lb, threshold_by_distance, group_threshold)
            beacons_this_dist = fixed_beacons.get(lb, {}) if fixed_beacons else {}

            for sample_id in sample_ids_for_lb:
                sample_df = gen_df[gen_df["Sample_Id"] == sample_id]

                points_global = sample_df[["Real", "Imag"]].values
                pc = sample_point_cloud(points_global, POINTS_PER_SAMPLE, rng)
                x = torch.from_numpy(pc).unsqueeze(0).to(device)
                logit = disc_model(x).item()
                disc_prob = float(torch.sigmoid(torch.tensor(logit)))
                disc_flag = disc_prob < DISC_THRESHOLD

                if beacons_this_dist:
                    v_sum = beacon_score_sum_with_fixed_beacons(sample_df, beacons_this_dist, eps)
                    beacon_flag = v_sum < threshold
                else:
                    v_sum = 0
                    beacon_flag = False

                #comb final
                final_flag = _resolve_final_flag(disc_flag, beacon_flag, experiment_mode)

                results.append({
                    "source": "generated.csv",
                    "target_distance": lb,
                    "distance": lb,
                    "sample_id": int(sample_id),
                    "disc_prob": disc_prob,
                    "disc_flag": int(disc_flag),
                    "v_score_sum": v_sum,
                    "threshold": threshold,
                    "beacon_flag": int(beacon_flag),
                    "final_flag": int(final_flag),
                    "experiment_mode": experiment_mode,
                })

    return pd.DataFrame(results)


