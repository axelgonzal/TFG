#injectar balisses (security local)
def inject_beacons_into_sample(sample_df, beacons_by_cluster):

    extra_rows = []
    next_symbol = int(sample_df["Symbol_Id"].max()) + 1
    sample_id = int(sample_df["Sample_Id"].iloc[0])

    for q, beacon_positions_local in beacons_by_cluster.items():
        cx, cy = _CONSTELLATION_CENTERS_DICT[int(q)]
        for (x_loc, y_loc) in beacon_positions_local:
            extra_rows.append({
                "Sample_Id": sample_id,
                "Symbol_Id": next_symbol,
                "Real": float(cx + x_loc),
                "Imag": float(cy + y_loc),
                "Q": int(q),
                "X_norm": float(x_loc),
                "Y_norm": float(y_loc),
            })
            next_symbol += 1

    return pd.concat([sample_df, pd.DataFrame(extra_rows)], ignore_index=True)


