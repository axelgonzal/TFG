def evaluate_reference_parallel(
    data_path,
    threshold_by_distance,
    group_threshold,
    disc_model,
    device,
    fixed_beacons,
    experiment_mode=EXPERIMENT_MODE,
    split_dict=None,
    partition="test",
    inject_at_eval=False,
):
    disc_model.eval()
    results = []
    rng = np.random.default_rng(SEED)

    with torch.no_grad():
        for source_file in list_source_files(data_path):
            path = os.path.join(data_path, source_file)
            df = pd.read_csv(path)

            if split_dict is not None:
                df = filter_samples_by_split(df, source_file, split_dict, partition)
            if df.empty:
                continue

            ref_distance = parse_distance_from_source(source_file)
            sample_ids = sorted(df["Sample_Id"].unique())

            for sample_id in sample_ids:
                sample_df = df[df["Sample_Id"] == sample_id]
                lb = ref_distance

                eps, _ = epsilon_for_distance(lb)
                threshold = threshold_for_distance(lb, threshold_by_distance, group_threshold)
                beacons_this_dist = fixed_beacons.get(lb, {}) if fixed_beacons else {}

                # Marcado activo: solo en experimento B
                if inject_at_eval and beacons_this_dist:
                    sample_df_eval = inject_beacons_into_sample(sample_df, beacons_this_dist)
                else:
                    sample_df_eval = sample_df

                # Discriminador
                points = sample_df_eval[["Real", "Imag"]].values
                pc = sample_point_cloud(points, POINTS_PER_SAMPLE, rng)
                x = torch.from_numpy(pc).unsqueeze(0).to(device)
                logit = disc_model(x).item()
                disc_prob = float(torch.sigmoid(torch.tensor(logit)))
                disc_flag = disc_prob < DISC_THRESHOLD

                # (solo si hay balizas)
                if beacons_this_dist:
                    v_sum = beacon_score_sum_with_fixed_beacons(sample_df_eval, beacons_this_dist, eps)
                    beacon_flag = v_sum < threshold
                else:
                    v_sum = 0
                    beacon_flag = False

                final_flag = _resolve_final_flag(disc_flag, beacon_flag, experiment_mode)

                results.append({
                    "source": source_file,
                    "target_distance": lb,
                    "distance": lb,
                    "sample_id": int(sample_id),
                    "disc_prob": disc_prob,
                    "disc_flag": int(disc_flag),
                    "v_score_sum": v_sum,
                    "threshold": threshold,
                    "beacon_flag": int(beacon_flag),
                    "final_flag": int(final_flag),
                    "experiment_mode": experiment_mode,
                })

    return pd.DataFrame(results)



