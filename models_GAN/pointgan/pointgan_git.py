#extrae num de dist
def parse_distance_from_source(source_file):
    match = re.search(r"source_(\d+)", source_file)
    if match is None:
        raise ValueError(f"Could not parse distance from {source_file}")
    return int(match.group(1))

#devuelve grupo distancia (para epsilon)
def group_for_distance(distance):
    for d_min, d_max, group in DISTANCE_BINS:
        if d_min <= distance < d_max:
            return group
    raise ValueError(f"Distance {distance} outside DISTANCE_BINS ranges")

#devuelve epsilon y grupo
def epsilon_for_distance(distance):
    group = group_for_distance(distance)
    return EPSILON_BY_GROUP[group], group

#combina para obtener epsilon, grupo y dist a partir del archivo
def epsilon_for_source(source_file):
    distance = parse_distance_from_source(source_file)
    eps, group = epsilon_for_distance(distance)
    return eps, group, distance


def list_source_files(data_path):
    files = [f for f in os.listdir(data_path) if f.startswith("source_") and f.endswith(".csv")]
    return sorted(files, key=parse_distance_from_source)


# Dentro de cada distancia, 80% train / 20% test
def build_train_test_split(data_path, test_fraction=TEST_FRACTION, seed=SEED):

    # {distance: {"train": [sample_ids], "test": [sample_ids]}}

    rng = np.random.default_rng(seed)
    split = {}
    for source_file in list_source_files(data_path):
        distance = parse_distance_from_source(source_file)
        path = os.path.join(data_path, source_file)
        df = pd.read_csv(path)
        sample_ids = sorted(df["Sample_Id"].unique())
        n = len(sample_ids)
        n_test = max(1, int(round(n * test_fraction)))
        shuffled = list(sample_ids)
        rng.shuffle(shuffled)
        test_ids = sorted(shuffled[:n_test])
        train_ids = sorted(shuffled[n_test:])

        # Verificaciones defensivas
        assert set(train_ids).isdisjoint(set(test_ids)), f"Distancia {distance}: train y test NO disjuntos"
        assert len(train_ids) + len(test_ids) == n, f"Distancia {distance}: train+test != total"

        split[distance] = {"train": train_ids, "test": test_ids}
    return split

#filtra samples dentro de una distancia (dependiendo particion)
def filter_samples_by_split(df, source_file, split_dict, partition="train"):

    distance = parse_distance_from_source(source_file)
    if distance not in split_dict:
        return df.iloc[0:0]
    valid_ids = set(split_dict[distance][partition])
    return df[df["Sample_Id"].isin(valid_ids)]


