def beacon_score_sum_with_fixed_beacons(sample_df, beacons_per_cluster, eps):
    if not beacons_per_cluster:
        return 0
    total = 0
    for q, beacon_positions in beacons_per_cluster.items():
        cluster_df = sample_df[sample_df["Q"] == q]
        if cluster_df.empty:
            continue
        count, _ = count_beacons(
            cluster_df["X_norm"].values,
            cluster_df["Y_norm"].values,
            beacon_positions,
            epsilon=eps,
        )
        total += count
    return total


#para el detector, combina decision
def _resolve_final_flag(disc_flag, beacon_flag, experiment_mode):
    if experiment_mode == "disc_only":
        return bool(disc_flag)
    if experiment_mode == "beacon_only":
        return bool(beacon_flag)
    if experiment_mode == "and":
        return bool(disc_flag and beacon_flag)
    return bool(disc_flag or beacon_flag)  # "or"


