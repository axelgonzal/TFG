class GeneratorAgent:

    def __init__(
        self,
        generator,
        sample_size,
        resolution
    ):

        self.generator = generator
        self.sample_size = sample_size
        self.resolution = resolution

    def generate(self,
                 num_points=128):

        latent = make_latent_samples(
            1,
            self.sample_size
        )
        #genera una predicción sintetica gracias al entreno previo
        generated_hist = self.generator.predict(latent)[0]
        # 64x64
        generated_hist = generated_hist.reshape(
            self.resolution,
            self.resolution
        )

        x_fake, y_fake = histogram_to_iq_points(
            generated_hist,
            num_points=num_points
        )

        return {
            "x": x_fake,
            "y": y_fake
        }

class DetectorAgent:

    def __init__(
        self,
        discriminator,
        resolution
    ):

        self.discriminator = discriminator
        self.resolution = resolution

    def detect(
        self,
        x,
        y
    ):

        H = iq_to_histogram(
            x,
            y,
            resolution=self.resolution
        )

        H_input = H.reshape(
            1,
            self.resolution * self.resolution
        )

        score = self.discriminator.predict(H_input)

        return score[0][0]

