def create_histogram(
    x,
    y,
    resolution=64,
    value_range=(-1,1)
):
#crear imagen
    H, _, _ = np.histogram2d(
        x,
        y,
        bins=resolution,
        range=[
            [value_range[0], value_range[1]],
            [value_range[0], value_range[1]]
        ]
    )
#Normalización
    if H.max() > 0:
        H = H / H.max()
#Ajuste a [-1,1] para tanh de GAN
    H = H * 2 - 1

    return H

